    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        //1.获取密码
        char[] pwds = this.txtPassword.getPassword();
        String password = new String(pwds);//将字符型数组转成字符串数组
        char[] pwdsConfirm = this.txtPasswordConfrim.getPassword();
        String passwordConfirm = new String(pwdsConfirm);
        char[] pwdsNew = this.txtNewPassword.getPassword();
        String passwordNew = new String(pwdsNew);
        //2.用户验证（调用业务访问数据库）
        User SearchUserName = ubiz.findByUsername(username);
        //非空验证
        if (StringUtil.checkLength(password) == false) {
            JOptionPane.showMessageDialog(this, "密码不能为空");
            return;
        }
        if (StringUtil.checkLength(passwordConfirm) == false) {
            JOptionPane.showMessageDialog(this, "确认密码不能为空");
            return;
        }
        if (StringUtil.checkLength(passwordNew) == false) {
            JOptionPane.showMessageDialog(this, "新密码不能为空");
            return;
        }
        //密码格式判断
        if (StringUtil.checkPassword(password) == false) {
            JOptionPane.showMessageDialog(this, "密码须由6-16位数字或英文字母组成");
            return;
        }
        //确认密码格式判断，一致判断
        if (StringUtil.checkPassword(passwordConfirm) == false) {
            JOptionPane.showMessageDialog(this, "密码须由6-16位数字或英文字母组成");
            return;
        }
        if (password.equals(passwordConfirm) == false) {
            JOptionPane.showMessageDialog(this, "两次输入密码应保持一致");
            return;
        }
        //新密码格式判断
        if (StringUtil.checkPassword(passwordNew) == false) {
            JOptionPane.showMessageDialog(this, "新密码须由6-16位数字或英文字母组成");
            return;
        }
        //旧密码正确判断
        if (password.equals(SearchUserName.getPassword()) == false) {
            JOptionPane.showMessageDialog(this, "旧密码错误");
            return;
        }
        //调用业务类
        boolean result = ubiz.updatePassword(SearchUserName.getUid(), passwordNew);
        if (result == true) {
            JOptionPane.showMessageDialog(this, "添加成功");
        } else {
            JOptionPane.showMessageDialog(this, "添加失败");
        }
        //清空并关闭
        clearInput();
        this.dispose();
    }//GEN-LAST:event_btnRegisterActionPerformed

