    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        //1.获取用户名和密码
        String username = this.txtUsername.getText();
        char[] pwds = this.txtPassword.getPassword();
        String password = new String(pwds);//将字符型数组转成字符串数组
        char[] pwdsConfirm = this.txtPasswordConfrim.getPassword();
        String passwordConfirm = new String(pwdsConfirm);
        String empids = this.txtEmpid.getText().trim();
        //2.用户验证（调用业务访问数据库）
        User SearchUserName = ubiz.findByUsername(username);
        //非空验证
        if (StringUtil.checkLength(username)==false) {
            JOptionPane.showMessageDialog(this, "用户名不能为空");
            return;
        }
        if (StringUtil.checkLength(password)==false) {
            JOptionPane.showMessageDialog(this, "密码不能为空");
            return;
        }
        if (StringUtil.checkLength(passwordConfirm)==false) {
            JOptionPane.showMessageDialog(this, "确认密码不能为空");
            return;
        }
        if (StringUtil.checkLength(empids)==false) {
            JOptionPane.showMessageDialog(this, "员工ID不能为空");
            return;
        }
        int empid = Integer.parseInt(empids);
        //用户名存在判断，格式判断
        if (StringUtil.checkUsername(username)==false) {
            JOptionPane.showMessageDialog(this, "用户名须由8-20位数字或英文字母组成");
            return;
        }
        if (SearchUserName != null) {
            JOptionPane.showMessageDialog(this, "用户名已存在");
            return;
        }
        //密码格式判断
        if (StringUtil.checkPassword(password)==false) {
            JOptionPane.showMessageDialog(this, "密码须由6-16位数字或英文字母组成");
            return;
        }
        //确认密码格式判断，一致判断
        if (StringUtil.checkPassword(passwordConfirm)==false) {
            JOptionPane.showMessageDialog(this, "密码须由6-16位数字或英文字母组成");
            return;
        }
        if (password.equals(passwordConfirm)==false) {
            JOptionPane.showMessageDialog(this, "两次输入密码应保持一致");
            return;
        }
        Employee SearchEmpid_Emp = ebiz.findByID(empid);
        User SearchEmpid_User =ubiz.findByEmpid(empid);
        if (SearchEmpid_Emp == null) {
            JOptionPane.showMessageDialog(this, "员工ID不存在");
            return;
        }
        if (SearchEmpid_User != null) {
            JOptionPane.showMessageDialog(this, "该员工ID已关联其他账号");
            return;
        }
        //组合对象
        User u = new User(null,username,password,empid,SearchEmpid_Emp.getEtype());
        //调用业务类
        boolean result = ubiz.add(u);
        if (result == true) {
            JOptionPane.showMessageDialog(this, "添加成功");
        } else {
            JOptionPane.showMessageDialog(this, "添加失败");
        }
        //清空并关闭
        clearInput();
        this.dispose();
    }//GEN-LAST:event_btnRegisterActionPerformed
