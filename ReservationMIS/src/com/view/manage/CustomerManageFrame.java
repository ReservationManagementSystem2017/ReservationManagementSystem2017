    public void showOnTable(List<Customer> list) {
        //1.获取表格模型
        DefaultTableModel dtm = (DefaultTableModel) this.tblCustomer.getModel();
        //2.清空表格信息
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }
        //3.显示数据
        for (Customer c : list) {
            Vector vt = new Vector();
            vt.add(c.getCid());
            vt.add(c.getCname());
            vt.add(c.getCscore());
            vt.add(c.getCphone());

            dtm.addRow(vt);
        }
    }

