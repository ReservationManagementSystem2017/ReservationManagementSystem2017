    //小程序3.将list数据显示到表格上
    public void showOnTable(List<Table> list) {
        //1.获取指定表格模型
        DefaultTableModel dtm = (DefaultTableModel) this.tblProduct.getModel();
        //2.清空表格信息
        while(dtm.getRowCount()>0){
            dtm.removeRow(0);
        }
        //3.显示数据
        for(Table t :list){
            Vector vt = new Vector();
            vt.add(t.getTid());
            vt.add(t.getTseat());
            if(t.getTcondition() == 1){
                vt.add("空");
            }
            else   vt.add("满");
            vt.add(t.getRid());
            Room room = rbiz.findByID(t.getRid());
            vt.add(room.getRname());

            dtm.addRow(vt);
        }
    }
